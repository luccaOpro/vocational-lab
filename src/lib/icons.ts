/**
 * Devuelve el SVG completo como string, listo para inyectar en innerHTML.
 * Hereda el color del contenedor (stroke=currentColor).
 *
 * @param name nombre del icono
 * @param size lado en px (default 24)
 */
export function iconHtml(name: IconName, size = 24): string {
  const paths = ICON_PATHS[name] ?? "";
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.75" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${paths}</svg>`;
}
